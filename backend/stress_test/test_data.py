"""
压力测试数据：用户凭证生成 + 问题池

说明：
- 测试用户名为 testuser001 ~ testuser100，密码统一 test123456
- 问题池包含短/中/长三种长度的问题，模拟真实用户的提问习惯
- 每个虚拟用户会随机从问题池中抽取问题进行多轮对话
"""

# ============================================================
# 用户凭证生成
# ============================================================
# 解释：压测时每个虚拟用户需要一个真实账号来登录
#       这里用代码生成 100 个用户的凭据，避免手动创建

TOTAL_USERS = 100
USER_PASSWORD = "test123456"  # 所有测试用户的统一密码


def get_username(index: int) -> str:
    """
    获取第 index 个测试用户的用户名
    格式：testuser001 ~ testuser100
    """
    return f"testuser{index:03d}"


def get_all_users() -> list[dict]:
    """
    返回所有测试用户的凭据列表
    每个元素是 {"username": "testuser001", "password": "test123456"}
    """
    return [
        {"username": get_username(i), "password": USER_PASSWORD}
        for i in range(1, TOTAL_USERS + 1)
    ]


# ============================================================
# 问题池
# ============================================================
# 解释：模拟不同类型的用户提问，让压测更接近真实场景
#       - 短问题：几个字的简单询问（检索快）
#       - 中问题：一句完整的句子（最常见）
#       - 长问题：多句话的复杂问题（检索内容多，回答长）

QUESTION_POOL = [
    # ---- 短问题（简单、检索快）----
    "iPhone 15 有什么新功能？",
    "这个产品保修多久？",
    "支持哪些支付方式？",
    "什么时候发货？",
    "有优惠活动吗？",

    # ---- 中问题（最常见的提问方式）----
    "我想了解一下这款手机的拍照效果怎么样？",
    "退货流程是怎么样的，需要什么条件？",
    "这个商品和上一代相比有哪些升级？",
    "请问有没有适合学生用的笔记本电脑推荐？",
    "会员等级是怎么划分的，有什么权益？",

    # ---- 长问题（包含多个子问题，回答更长）----
    "我最近想买一台新手机，预算在3000到5000之间，"
    "主要用来拍照和打游戏，屏幕要大一点，续航也要好，有什么推荐的吗？",

    "我想退货，但是已经过了7天无理由退货的期限了，"
    "不过商品确实有质量问题，这种情况应该怎么处理？能不能特殊申请退货？",

    "对比一下这几款产品的主要区别，包括价格、性能、续航、屏幕、"
    "拍照等各个方面，哪个性价比最高？适合什么样的人群购买？",
]


def get_question_pool() -> list[str]:
    """返回完整问题池"""
    return QUESTION_POOL
